    public void limpar(){
        tfPlaca.setText("");
        tfModelo.setText("");
        tfPlaca.requestFocus();
    }
    
    public void inserir(){
        try{
            v = new Veiculo();

            v.setPlaca(Integer.parseInt(tfPlaca.getText()));
            v.setModelo(tfModelo.getText());

            v = bd.insereVec(v);

            if(v != null){
                JOptionPane.showMessageDialog(
                        null,
                        "Veiculo cadastrado com sucesso!",
                        "Cadastro de Veiculo",
                        JOptionPane.INFORMATION_MESSAGE
                );
                limpar();
            }
            else{
                JOptionPane.showMessageDialog(
                        null,
                        "Essa placa jÃ¡ estÃ¡ cadastrada!",
                        "Cadastro de Veiculo - ERRO",
                        JOptionPane.ERROR_MESSAGE
                );
                tfPlaca.setText("");
                tfPlaca.requestFocus();
            }
        }
        catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(
                        null,
                        "A placa deve ser um nÃºmero inteiro!",
                        "Cadastro de Veiculo - ERRO",
                        JOptionPane.ERROR_MESSAGE
                );
                tfPlaca.setText("");
                tfPlaca.requestFocus();
        }
    }
    
