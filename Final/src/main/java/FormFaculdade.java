    public void limpar(){
        tfNum.setText("");
        tfNome.setText("");
        tfRua.setText("");
        tfNum.requestFocus();
    }
    
    public void inserir(){
        try{
            f = new Faculdade();

            f.setNum(Integer.parseInt(tfNum.getText()));
            f.setNome(tfNome.getText());
            f.setRua(tfRua.getText());

            f = bd.insereFac(f);

            if(f != null){
                JOptionPane.showMessageDialog(
                        null,
                        "Faculdade cadastrada com sucesso!",
                        "Cadastro de Faculdade",
                        JOptionPane.INFORMATION_MESSAGE
                );
                limpar();
            }
            else{
                JOptionPane.showMessageDialog(
                        null,
                        "JÃ¡ existe uma Faculdade cadastrada com esse ID!",
                        "Cadastro de Faculdade - ERRO",
                        JOptionPane.ERROR_MESSAGE
                );
                tfNum.setText("");
                tfNum.requestFocus();
            }
        }
        catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(
                        null,
                        "O nÃºmero deve ser um inteiro!",
                        "Cadastro de Faculdade - ERRO",
                        JOptionPane.ERROR_MESSAGE
                );
                tfNum.setText("");
                tfNum.requestFocus();
        }
    }
    
